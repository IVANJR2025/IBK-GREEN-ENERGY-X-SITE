/* aplica traduções */
function applyLang(lang) {
  const dict = I18N[lang] || I18N.pt;
  document.documentElement.lang = lang === "pt" ? "pt-PT" : lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const k = el.getAttribute("data-i18n");
    if (dict[k] !== undefined) el.innerHTML = dict[k];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const k = el.getAttribute("data-i18n-placeholder");
    if (dict[k] !== undefined) el.setAttribute("placeholder", dict[k]);
  });
  document.querySelectorAll(".lang-switch button").forEach((b) => {
    b.classList.toggle("active", b.dataset.lang === lang);
  });
  localStorage.setItem("ibk-lang", lang);
  window.IBK_LANG = lang;
  document.dispatchEvent(new CustomEvent("ibk:langchange", { detail: lang }));
}

function t(key) {
  const dict = I18N[window.IBK_LANG] || I18N.pt;
  return dict[key] !== undefined ? dict[key] : key;
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("ibk-lang") || "pt";
  applyLang(saved);
  document.querySelectorAll(".lang-switch button").forEach((b) => {
    b.addEventListener("click", () => applyLang(b.dataset.lang));
  });
});
